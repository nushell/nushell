fn theme_cmd(theme: &str, footer: bool, then: &str) -> String {
    let with_footer = if footer {
        "$env.config.footer_mode = \"always\"\n"
    } else {
        ""
    };
    format!(
        "$env.config.table.mode = \"{theme}\"\n$env.config.table.header_on_separator = true\n{with_footer}{then}"
    )
}

